//! Runtime code generation, McGavin-style.
//!
//! McGavin's speed comes from regenerating a puzzle-specialised program (`body.c`
//! via `genbody -DG`), where the fill order and the pieces are baked in so the
//! hot loop has no `free[level]`/`cursor[level]` indirection and no `%W` border
//! tests — cell structure is straight-line, neighbour addresses are constants.
//!
//! [`emit_program`] produces a self-contained Rust program (no external crates,
//! so `rustc` compiles it standalone and fast) that:
//!   * bakes the piece edge table, the `(up,left)` candidate buckets, and the
//!     pinned seed board as `const` data,
//!   * unrolls the free-cell fill as a `loop { match state { … } }` state machine
//!     — Rust's goto-free equivalent of McGavin's `goto cell_R_C_next` — with the
//!     up/left neighbour positions hardcoded per free cell,
//!   * runs the search under a wall-clock budget and prints the same `RESULT`
//!     line the other engines print, so the harness reads it unchanged.
//!
//! The caller compiles the emitted source with `rustc -O` and runs it (see the
//! `run_dfs_codegen_jit` binary). This is the true-codegen path; the in-process
//! [`crate::run`] stays as the portable table-driven engine.

use e2_io::Instance;

const W: usize = 16;
const N: usize = W * W;

/// Emit a self-contained specialised Rust program for `inst`. `budget_ms` is the
/// wall-clock budget baked into the program.
#[must_use]
pub fn emit_program(inst: &Instance, budget_ms: u64) -> String {
    // ---- Build the same structures the table-driven engine builds, but bake
    // them as source-level constants. ----
    let colors = inst.pieces.max_color() as usize + 1;

    // Oriented pieces, packed like crate::Packed: pid<<48 | rot<<40 | edges<<8.
    let mut oriented: Vec<u64> = Vec::new();
    for (pid, p) in inst.pieces.iter() {
        let mut seen: Vec<[u8; 4]> = Vec::with_capacity(4);
        for rot in 0..4u8 {
            let e = p.rotated(rot);
            if seen.contains(&e) {
                continue;
            }
            seen.push(e);
            let edges = (u32::from(e[0]) << 24)
                | (u32::from(e[1]) << 16)
                | (u32::from(e[2]) << 8)
                | u32::from(e[3]);
            oriented.push((u64::from(pid) << 48) | (u64::from(rot) << 40) | (u64::from(edges) << 8));
        }
    }

    // (up,left) buckets and (up-only) buckets, flattened with a u64::MAX sentinel.
    let mut buckets_ul: Vec<Vec<u64>> = vec![Vec::new(); colors * colors];
    let mut buckets_u: Vec<Vec<u64>> = vec![Vec::new(); colors];
    let mut all: Vec<u64> = Vec::new();
    for &w in &oriented {
        let e = [
            (w >> 32) as u8,
            (w >> 24) as u8,
            (w >> 16) as u8,
            (w >> 8) as u8,
        ];
        buckets_ul[e[0] as usize * colors + e[3] as usize].push(w);
        buckets_u[e[0] as usize].push(w);
        all.push(w);
    }
    all.push(u64::MAX);
    let (ul_start, pool_ul) = flatten(&buckets_ul);
    let (u_start, pool_u) = flatten(&buckets_u);

    // Pins: which cells are pinned, and the (packed) piece placed there.
    let mut pinned = [false; N];
    let mut pin_word = [u64::MAX; N]; // packed word of the pinned piece, per cell
    for h in &inst.hints {
        pinned[h.pos as usize] = true;
        let e = inst
            .pieces
            .get(h.piece)
            .expect("hint piece id in range")
            .rotated(h.rot);
        let edges = (u32::from(e[0]) << 24)
            | (u32::from(e[1]) << 16)
            | (u32::from(e[2]) << 8)
            | u32::from(e[3]);
        pin_word[h.pos as usize] =
            (u64::from(h.piece) << 48) | (u64::from(h.rot) << 40) | (u64::from(edges) << 8);
    }
    let free: Vec<usize> = (0..N).filter(|&p| !pinned[p]).collect();

    // ---- Emit the program. ----
    let mut s = String::new();
    s.push_str("// @generated by dfs-codegen::emit — puzzle-specialised backtracker.\n");
    s.push_str("#![allow(clippy::all, clippy::pedantic, unused)]\n");
    s.push_str("use std::time::Instant;\n");
    s.push_str(&format!("const W: usize = {W};\nconst N: usize = {N};\n"));
    s.push_str(&format!("const COLORS: usize = {colors};\n"));
    s.push_str(&format!("const BUDGET_MS: u64 = {budget_ms};\n"));
    s.push_str(&format!("const NP: usize = {};\n", inst.pieces.len()));
    s.push_str(&format!("const MAX_SCORE: u32 = {};\n", 2 * W * W - W - W));

    emit_slice_u64(&mut s, "POOL_UL", &pool_ul);
    emit_slice_u32(&mut s, "UL_START", &ul_start);
    emit_slice_u64(&mut s, "POOL_U", &pool_u);
    emit_slice_u32(&mut s, "U_START", &u_start);
    emit_slice_u64(&mut s, "ALL", &all);

    // Seed board: for each cell, packed word if pinned else u64::MAX.
    emit_slice_u64(&mut s, "PIN_WORD", &pin_word);

    // Free-cell sequence and its per-cell up/left neighbour positions (baked).
    let up_pos: Vec<i32> = free
        .iter()
        .map(|&p| if p < W { -1 } else { (p - W) as i32 })
        .collect();
    let left_pos: Vec<i32> = free
        .iter()
        .map(|&p| if p % W == 0 { -1 } else { (p - 1) as i32 })
        .collect();
    // A free cell only needs its down/right edge checked at placement if that
    // neighbour is ALREADY fixed when the cell is filled: the rim (off-board, must
    // be border 0) or a PINNED interior cell. If the down/right neighbour is
    // another free cell it is still empty at placement (row-major over free cells
    // fills it later), so no check is needed. Baking these flags means the common
    // unhinted / interior case pays nothing for the down/right correctness checks.
    let need_d: Vec<u8> = free
        .iter()
        .map(|&p| u8::from(p + W >= N || (p + W < N && pinned[p + W])))
        .collect();
    let need_r: Vec<u8> = free
        .iter()
        .map(|&p| u8::from(p % W == W - 1 || (p % W != W - 1 && pinned[p + 1])))
        .collect();
    emit_slice_usize(&mut s, "FREE", &free);
    emit_slice_i32(&mut s, "FREE_UP", &up_pos);
    emit_slice_i32(&mut s, "FREE_LEFT", &left_pos);
    emit_slice_u8(&mut s, "FREE_NEED_D", &need_d);
    emit_slice_u8(&mut s, "FREE_NEED_R", &need_r);
    s.push_str(&format!("const DEPTH: usize = {};\n", free.len()));
    s.push_str(&format!("const PINS: u32 = {};\n", inst.hints.len()));

    s.push_str(RUNTIME);
    s
}

/// Flatten buckets into (start-offsets, pool) with a `u64::MAX` sentinel per
/// bucket. Empty buckets point at a shared sentinel-only run.
fn flatten(buckets: &[Vec<u64>]) -> (Vec<u32>, Vec<u64>) {
    let mut start = Vec::with_capacity(buckets.len());
    let mut pool = Vec::new();
    for b in buckets {
        start.push(pool.len() as u32);
        pool.extend_from_slice(b);
        pool.push(u64::MAX);
    }
    (start, pool)
}

fn emit_slice_u64(s: &mut String, name: &str, v: &[u64]) {
    s.push_str(&format!("static {name}: [u64; {}] = [", v.len()));
    for x in v {
        s.push_str(&format!("{x},"));
    }
    s.push_str("];\n");
}
fn emit_slice_u32(s: &mut String, name: &str, v: &[u32]) {
    s.push_str(&format!("static {name}: [u32; {}] = [", v.len()));
    for x in v {
        s.push_str(&format!("{x},"));
    }
    s.push_str("];\n");
}
