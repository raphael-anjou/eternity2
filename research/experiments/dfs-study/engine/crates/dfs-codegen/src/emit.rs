//! Runtime code generation, McGavin-style.
//!
//! McGavin's speed comes from regenerating a puzzle-specialised program (`body.c`
//! via `genbody -DG`), where the fill order and the pieces are baked in so the
//! hot loop has no `free[level]`/`cursor[level]` indirection and no `%W` border
//! tests — cell structure is straight-line, neighbour addresses are constants.
//!
//! [`emit_program`] produces a self-contained Rust program (no external crates,
//! so `rustc` compiles it standalone and fast) that:
//!   * bakes the piece edge table, the `(up,left)` candidate buckets, and the
//!     pinned seed board as `const` data,
//!   * unrolls the free-cell fill as a `loop { match state { … } }` state machine
//!     — Rust's goto-free equivalent of McGavin's `goto cell_R_C_next` — with the
//!     up/left neighbour positions hardcoded per free cell,
//!   * runs the search under a wall-clock budget and prints the same `RESULT`
//!     line the other engines print, so the harness reads it unchanged.
//!
//! The caller compiles the emitted source with `rustc -O` and runs it (see the
//! `run_dfs_codegen_jit` binary). This is the true-codegen path; the in-process
//! [`crate::run`] stays as the portable table-driven engine.

use e2_io::Instance;

const W: usize = 16;
const N: usize = W * W;

/// Emit a self-contained specialised Rust program for `inst`. `budget_ms` is the
/// wall-clock budget baked into the program.
#[must_use]
pub fn emit_program(inst: &Instance, budget_ms: u64) -> String {
    // ---- Build the same structures the table-driven engine builds, but bake
    // them as source-level constants. ----
    let colors = inst.pieces.max_color() as usize + 1;

    // Oriented pieces, packed like crate::Packed: pid<<48 | rot<<40 | edges<<8.
    let mut oriented: Vec<u64> = Vec::new();
    for (pid, p) in inst.pieces.iter() {
        let mut seen: Vec<[u8; 4]> = Vec::with_capacity(4);
        for rot in 0..4u8 {
            let e = p.rotated(rot);
            if seen.contains(&e) {
                continue;
            }
            seen.push(e);
            let edges = (u32::from(e[0]) << 24)
                | (u32::from(e[1]) << 16)
                | (u32::from(e[2]) << 8)
                | u32::from(e[3]);
            oriented.push((u64::from(pid) << 48) | (u64::from(rot) << 40) | (u64::from(edges) << 8));
        }
    }

    // (up,left) buckets and (up-only) buckets, flattened with a u64::MAX sentinel.
    let mut buckets_ul: Vec<Vec<u64>> = vec![Vec::new(); colors * colors];
    let mut buckets_u: Vec<Vec<u64>> = vec![Vec::new(); colors];
    let mut all: Vec<u64> = Vec::new();
    for &w in &oriented {
        let e = [
            (w >> 32) as u8,
            (w >> 24) as u8,
            (w >> 16) as u8,
            (w >> 8) as u8,
        ];
        buckets_ul[e[0] as usize * colors + e[3] as usize].push(w);
        buckets_u[e[0] as usize].push(w);
        all.push(w);
    }
    all.push(u64::MAX);
    let (ul_start, pool_ul) = flatten(&buckets_ul);
    let (u_start, pool_u) = flatten(&buckets_u);

    // Pins: which cells are pinned, and the (packed) piece placed there.
    let mut pinned = [false; N];
    let mut pin_word = [u64::MAX; N]; // packed word of the pinned piece, per cell
    for h in &inst.hints {
        pinned[h.pos as usize] = true;
        let e = inst
            .pieces
            .get(h.piece)
            .expect("hint piece id in range")
            .rotated(h.rot);
        let edges = (u32::from(e[0]) << 24)
            | (u32::from(e[1]) << 16)
            | (u32::from(e[2]) << 8)
            | u32::from(e[3]);
        pin_word[h.pos as usize] =
            (u64::from(h.piece) << 48) | (u64::from(h.rot) << 40) | (u64::from(edges) << 8);
    }
    let free: Vec<usize> = (0..N).filter(|&p| !pinned[p]).collect();

    // ---- Emit the program. ----
    let mut s = String::new();
    s.push_str("// @generated by dfs-codegen::emit — puzzle-specialised backtracker.\n");
    s.push_str("#![allow(clippy::all, clippy::pedantic, unused)]\n");
    s.push_str("use std::time::Instant;\n");
    s.push_str(&format!("const W: usize = {W};\nconst N: usize = {N};\n"));
    s.push_str(&format!("const COLORS: usize = {colors};\n"));
    s.push_str(&format!("const BUDGET_MS: u64 = {budget_ms};\n"));
    s.push_str(&format!("const NP: usize = {};\n", inst.pieces.len()));
    s.push_str(&format!("const MAX_SCORE: u32 = {};\n", 2 * W * W - W - W));

    emit_slice_u64(&mut s, "POOL_UL", &pool_ul);
    emit_slice_u32(&mut s, "UL_START", &ul_start);
    emit_slice_u64(&mut s, "POOL_U", &pool_u);
    emit_slice_u32(&mut s, "U_START", &u_start);
    emit_slice_u64(&mut s, "ALL", &all);

    // Seed board: for each cell, packed word if pinned else u64::MAX.
    emit_slice_u64(&mut s, "PIN_WORD", &pin_word);

    // Free-cell sequence and its per-cell up/left neighbour positions (baked).
    let up_pos: Vec<i32> = free
        .iter()
        .map(|&p| if p < W { -1 } else { (p - W) as i32 })
        .collect();
    let left_pos: Vec<i32> = free
        .iter()
        .map(|&p| if p % W == 0 { -1 } else { (p - 1) as i32 })
        .collect();
    // A free cell only needs its down/right edge checked at placement if that
    // neighbour is ALREADY fixed when the cell is filled: the rim (off-board, must
    // be border 0) or a PINNED interior cell. If the down/right neighbour is
    // another free cell it is still empty at placement (row-major over free cells
    // fills it later), so no check is needed. Baking these flags means the common
    // unhinted / interior case pays nothing for the down/right correctness checks.
    let need_d: Vec<u8> = free
        .iter()
        .map(|&p| u8::from(p + W >= N || (p + W < N && pinned[p + W])))
        .collect();
    let need_r: Vec<u8> = free
        .iter()
        .map(|&p| u8::from(p % W == W - 1 || (p % W != W - 1 && pinned[p + 1])))
        .collect();
    emit_slice_usize(&mut s, "FREE", &free);
    emit_slice_i32(&mut s, "FREE_UP", &up_pos);
    emit_slice_i32(&mut s, "FREE_LEFT", &left_pos);
    emit_slice_u8(&mut s, "FREE_NEED_D", &need_d);
    emit_slice_u8(&mut s, "FREE_NEED_R", &need_r);
    s.push_str(&format!("const DEPTH: usize = {};\n", free.len()));
    s.push_str(&format!("const PINS: u32 = {};\n", inst.hints.len()));

    s.push_str(RUNTIME);
    s
}

/// Emit the function-chain engine: one specialised `#[inline(never)]` function
/// per free cell, baking that cell's position, neighbour positions, pool base
/// and down/right-check requirement as constants. Advancing calls the next
/// cell's function; backtracking is a plain return; a `true` return means the
/// time budget expired and unwinds the whole chain. This reproduces McGavin's
/// straight-line per-cell code as a chain of tiny functions — which rustc
/// compiles in ~1s (no single huge function), unlike a monolithic `match` that
/// hits a pathological compile cliff and which rustc cannot lower to a computed
/// goto (rust-lang/rust#80630).
#[must_use]
pub fn emit_program_chain(inst: &Instance, budget_ms: u64) -> String {
    let colors = inst.pieces.max_color() as usize + 1;
    let mut oriented: Vec<u64> = Vec::new();
    for (pid, p) in inst.pieces.iter() {
        let mut seen: Vec<[u8; 4]> = Vec::with_capacity(4);
        for rot in 0..4u8 {
            let e = p.rotated(rot);
            if seen.contains(&e) {
                continue;
            }
            seen.push(e);
            let edges = (u32::from(e[0]) << 24)
                | (u32::from(e[1]) << 16)
                | (u32::from(e[2]) << 8)
                | u32::from(e[3]);
            oriented.push((u64::from(pid) << 48) | (u64::from(rot) << 40) | (u64::from(edges) << 8));
        }
    }
    let mut buckets_ul: Vec<Vec<u64>> = vec![Vec::new(); colors * colors];
    for &w in &oriented {
        let e0 = (w >> 32) as u8;
        let e3 = (w >> 8) as u8;
        buckets_ul[e0 as usize * colors + e3 as usize].push(w);
    }
    let (ul_start, pool_ul) = flatten(&buckets_ul);

    let mut pinned = [false; N];
    let mut pin_word = [u64::MAX; N];
    for h in &inst.hints {
        pinned[h.pos as usize] = true;
        let e = inst.pieces.get(h.piece).expect("hint piece id").rotated(h.rot);
        let edges = (u32::from(e[0]) << 24)
            | (u32::from(e[1]) << 16)
            | (u32::from(e[2]) << 8)
            | u32::from(e[3]);
        pin_word[h.pos as usize] =
            (u64::from(h.piece) << 48) | (u64::from(h.rot) << 40) | (u64::from(edges) << 8);
    }
    let free: Vec<usize> = (0..N).filter(|&p| !pinned[p]).collect();

    let mut s = String::new();
    s.push_str("// @generated by dfs-codegen::emit_program_chain — function-chain backtracker.\n");
    s.push_str("#![allow(clippy::all, clippy::pedantic, unused)]\n");
    s.push_str("use std::time::Instant;\n");
    s.push_str(&format!("const W: usize = {W};\nconst N: usize = {N};\nconst COLORS: usize = {colors};\n"));
    s.push_str(&format!("const NP: usize = {};\n", inst.pieces.len()));
    s.push_str(&format!("const MAX_SCORE: u32 = {};\n", 2 * W * W - W - W));
    s.push_str(&format!("const BUDGET_MS: u64 = {budget_ms};\n"));
    s.push_str(&format!("const PINS: u32 = {};\n", inst.hints.len()));
    emit_slice_u64(&mut s, "POOL_UL", &pool_ul);
    emit_slice_u32(&mut s, "UL_START", &ul_start);
    emit_slice_u64(&mut s, "PIN_WORD", &pin_word);

    s.push_str(CHAIN_PRELUDE);

    // One function per free cell.
    for (k, &pos) in free.iter().enumerate() {
        let r = pos % W;
        let up_p: i64 = if pos < W { -1 } else { (pos - W) as i64 };
        let left_p: i64 = if pos % W == 0 { -1 } else { (pos - 1) as i64 };
        let need_d = pos + W >= N || (pos + W < N && pinned[pos + W]);
        let need_r = pos % W == W - 1 || (pos % W != W - 1 && pinned[pos + 1]);
        // Optimisation: the left neighbour is the CELL PLACED BY THE CALLER when
        // it is the immediately-preceding free cell in the chain AND is our
        // horizontal left (pos-1). In that (94% common) case the caller passes its
        // placed right edge as `left_arg`, so this cell skips a board read.
        let left_from_arg = pos % W != 0 && k > 0 && free[k - 1] == pos - 1;
        // Whether the NEXT cell in the chain is our horizontal-right neighbour, in
        // which case we pass our right edge to it as its `left_arg`.
        let next_takes_arg =
            k + 1 < free.len() && (pos + 1) % W != 0 && free[k + 1] == pos + 1;
        let next = if k + 1 < free.len() {
            format!("cell_{}", k + 1)
        } else {
            "terminal".to_string()
        };
        s.push_str(&format!("#[inline(never)]\nfn cell_{k}(st: &mut St, left_arg: u8) -> bool {{\n"));
        // Budget check: read a flag a background timer thread flips at the
        // deadline. This is a single relaxed atomic LOAD — no `Instant::now` call
        // — so the cell functions stay leaf-ish and don't spill the whole
        // callee-saved register set on every call (which is McGavin's edge). The
        // flag is checked every cell entry, cheaply.
        s.push_str("    if TIMED_OUT.load(Ordering::Relaxed) { return true; }\n");
        // up/left colours
        let up_expr = if up_p < 0 { "0u8".to_string() } else { format!("(unsafe {{ *st.cell.get_unchecked({up_p}) }} >> 8) as u8") };
        let left_expr = if left_from_arg {
            "left_arg".to_string()
        } else if left_p < 0 {
            "0u8".to_string()
        } else {
            format!("(unsafe {{ *st.cell.get_unchecked({left_p}) }} >> 16) as u8")
        };
        s.push_str(&format!("    let _ = left_arg;\n    let up = {up_expr}; let left = {left_expr};\n"));
        s.push_str("    let base = unsafe { *UL_START.get_unchecked(up as usize * COLORS + left as usize) } as usize;\n");
        s.push_str("    let mut c = 0usize;\n");
        s.push_str("    loop {\n");
        s.push_str("        let w = unsafe { *POOL_UL.get_unchecked(base + c) };\n");
        s.push_str("        if w == u64::MAX { break; }\n");
        s.push_str("        c += 1;\n");
        s.push_str("        let pid = (w >> 48) as usize;\n");
        s.push_str("        if unsafe { *st.used.get_unchecked(pid >> 6) } & (1u64 << (pid & 63)) != 0 { continue; }\n");
        s.push_str("        let ew = (w >> 8) as u32;\n");
        s.push_str("        let e_up = (ew>>24) as u8; let e_r = (ew>>16) as u8; let e_d = (ew>>8) as u8; let e_l = ew as u8;\n");
        if need_d {
            if pos + W >= N {
                s.push_str("        if e_d != 0 { continue; }\n");
            } else {
                s.push_str(&format!("        {{ let cn = unsafe {{ *st.cell.get_unchecked({}) }}; if cn != CELL_EMPTY && e_d != (cn>>24) as u8 {{ continue; }} }}\n", pos + W));
            }
        }
        if need_r {
            if r == W - 1 {
                s.push_str("        if e_r != 0 { continue; }\n");
            } else {
                s.push_str(&format!("        {{ let cn = unsafe {{ *st.cell.get_unchecked({}) }}; if cn != CELL_EMPTY && e_r != cn as u8 {{ continue; }} }}\n", pos + 1));
            }
        }
        s.push_str("        st.nodes += 1;\n");
        s.push_str("        let mut gain = 0u32;\n");
        if pos >= W {
            s.push_str(&format!("        {{ let cn = unsafe {{ *st.cell.get_unchecked({}) }}; if cn!=CELL_EMPTY && e_up!=0 && e_up==(cn>>8) as u8 {{ gain+=1; }} }}\n", pos - W));
        }
        if pos + W < N {
            s.push_str(&format!("        {{ let cn = unsafe {{ *st.cell.get_unchecked({}) }}; if cn!=CELL_EMPTY && e_d!=0 && e_d==(cn>>24) as u8 {{ gain+=1; }} }}\n", pos + W));
        }
        if r != 0 {
            s.push_str(&format!("        {{ let cn = unsafe {{ *st.cell.get_unchecked({}) }}; if cn!=CELL_EMPTY && e_l!=0 && e_l==(cn>>16) as u8 {{ gain+=1; }} }}\n", pos - 1));
        }
        if r != W - 1 {
            s.push_str(&format!("        {{ let cn = unsafe {{ *st.cell.get_unchecked({}) }}; if cn!=CELL_EMPTY && e_r!=0 && e_r==cn as u8 {{ gain+=1; }} }}\n", pos + 1));
        }
        s.push_str(&format!("        unsafe {{ *st.cell.get_unchecked_mut({pos}) = ew; }}\n"));
        s.push_str("        unsafe { *st.used.get_unchecked_mut(pid >> 6) |= 1u64 << (pid & 63); }\n");
        s.push_str("        let saved = st.score; st.score += gain;\n");
        s.push_str("        if st.score > st.best { st.best = st.score; }\n");
        // Pass our right edge to the next cell iff it is our horizontal-right
        // neighbour (so it can use it as its left constraint without a board read);
        // otherwise the arg is unused there and 0 is fine.
        let next_arg = if next_takes_arg { "e_r" } else { "0" };
        s.push_str(&format!("        if {next}(st, {next_arg}) {{ return true; }}\n"));
        s.push_str("        st.score = saved;\n");
        s.push_str("        unsafe { *st.used.get_unchecked_mut(pid >> 6) &= !(1u64 << (pid & 63)); }\n");
        s.push_str(&format!("        unsafe {{ *st.cell.get_unchecked_mut({pos}) = CELL_EMPTY; }}\n"));
        s.push_str("    }\n");
        s.push_str("    false\n");
        s.push_str("}\n");
    }

    // Entry point into the chain (or a no-op when every cell is pinned). cell_0's
    // left is the border (0) if it is in column 0, else read once from the board.
    if free.is_empty() {
        s.push_str("#[inline(always)]\nfn run_search(_st: &mut St) {}\n");
    } else {
        let first = free[0];
        let l0 = if first % W == 0 {
            "0u8".to_string()
        } else {
            format!("(unsafe {{ *st.cell.get_unchecked({}) }} >> 16) as u8", first - 1)
        };
        s.push_str(&format!("#[inline(always)]\nfn run_search(st: &mut St) {{ let l0 = {l0}; cell_0(st, l0); }}\n"));
    }

    s.push_str(CHAIN_MAIN);
    s
}

/// Flatten buckets into (start-offsets, pool) with a `u64::MAX` sentinel per
/// bucket. Empty buckets point at a shared sentinel-only run.
fn flatten(buckets: &[Vec<u64>]) -> (Vec<u32>, Vec<u64>) {
    let mut start = Vec::with_capacity(buckets.len());
    let mut pool = Vec::new();
    for b in buckets {
        start.push(pool.len() as u32);
        pool.extend_from_slice(b);
        pool.push(u64::MAX);
    }
    (start, pool)
}

fn emit_slice_u64(s: &mut String, name: &str, v: &[u64]) {
    s.push_str(&format!("static {name}: [u64; {}] = [", v.len()));
    for x in v {
        s.push_str(&format!("{x},"));
    }
    s.push_str("];\n");
}
